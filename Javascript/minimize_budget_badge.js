// Minimizes the budget badge on every page except the home page. 
// https://app.asana.com/0/1201388388286192/1202013907212186
window.addEventListener("load", (event) => {

    let onHomePage = document.getElementById('home');
    if (!(onHomePage)) {
        document.querySelector("#badgeLiner > a.minimize_logos").click();
    }

});